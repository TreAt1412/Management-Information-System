package model;

/**
 *
 * @author Huy_Dat
 */
public class Account {
    int id;
    String username;
    String password;
    int companyID;
    int role;
    String email;

    public Account() {
    }

	public Account(int id, String username, String password, int companyID, int role, String email) {
		super();
		this.id = id;
		this.username = username;
		this.password = password;
		this.companyID = companyID;
		this.role = role;
		this.email = email;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public int getCompanyID() {
		return companyID;
	}

	public void setCompanyID(int companyID) {
		this.companyID = companyID;
	}

	public int getRole() {
		return role;
	}

	public void setRole(int role) {
		this.role = role;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

    
    
}
